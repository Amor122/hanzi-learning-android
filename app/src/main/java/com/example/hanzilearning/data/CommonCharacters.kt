    // ============ 工具函数：按级别字列表转换为 CharacterData ============
    private fun toDataList(level: Int, chars: List<String>): List<CharacterData> {
        val seen = mutableSetOf<String>()
        val result = mutableListOf<CharacterData>()
        for (c in chars) {
            if (seen.contains(c)) continue
            seen.add(c)
            val py = pinyinMap[c] ?: ""
            val m = meaningMap[c] ?: ""
            result.add(
                CharacterData(character = c, pinyin = py, meaning = m, level = level, strokeCount = 0, fullMeaning = m)
            )
        }
        return result
    }

    private val level1Data by lazy { toDataList(1, level1Chars) }
    private val level2Data by lazy { toDataList(2, level2Chars) }
    private val level3Data by lazy { toDataList(3, level3Chars) }
    private val level4Data by lazy { toDataList(4, level4Chars) }
    private val level5Data by lazy { toDataList(5, level5Chars) }

    /**
     * 根据级别返回汉字列表。级别 1~5 对应 一~五年级。
     * 每级的字与其他级严格不重复。
     */
    fun getByLevel(level: Int): List<CharacterData> {
        return when (level) {
            1 -> level1Data
            2 -> level2Data
            3 -> level3Data
            4 -> level4Data
            5 -> level5Data
            else -> level1Data
        }
    }

    /** 返回全部字（1~5 级合并），用于跟读学习等场景。 */
    fun getAll(): List<CharacterData> {
        return level1Data + level2Data + level3Data + level4Data + level5Data
    }

    /** 返回总字数，用于进度统计。 */
    fun totalCount(): Int = level1Chars.size + level2Chars.size + level3Chars.size + level4Chars.size + level5Chars.size
