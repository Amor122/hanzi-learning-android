    // 获取所有字符（去重，保持顺序）
    fun getAllCharacters(): List<CharacterData> {
        val seen = mutableSetOf<String>()
        val result = mutableListOf<CharacterData>()

        fun addLevel(pairs: List<Pair<String, String>>, level: Int) {
            for ((char, pinyin) in pairs) {
                if (!seen.contains(char)) {
                    seen.add(char)
                    val meaning = meanings[char] ?: char
                    result.add(
                        CharacterData(
                            character = char,
                            pinyin = pinyin,
                            meaning = meaning,
                            level = level
                        )
                    )
                }
            }
        }

        addLevel(level1, 1)
        addLevel(level2, 2)
        addLevel(level3, 3)
        addLevel(level4, 4)
        addLevel(level5, 5)

        return result
    }

    fun getByLevel(level: Int): List<CharacterData> {
        return getAllCharacters().filter { it.level == level }
    }

    fun getCharacter(char: String): CharacterData? {
        return getAllCharacters().firstOrNull { it.character == char }
    }

    fun getRandomDistractors(target: String, count: Int = 4): List<String> {
        val all = getAllCharacters().map { it.character }.filter { it != target }
        return all.shuffled().take(count)
    }

    fun getRandomPinyinDistractors(targetPinyin: String, count: Int = 4): List<String> {
        val all = getAllCharacters().map { it.pinyin }.filter { it != targetPinyin }.distinct()
        return all.shuffled().take(count)
    }

    fun totalCount(): Int = getAllCharacters().size
