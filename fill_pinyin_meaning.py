# 替换：找到原有 pinyinMap 和 meaningMap 并补全空字段
def fill_map(section_name, src_dict, extra_dict):
    global content
    m = re.search(r'private val ' + section_name + r' = mapOf\(([\s\S]+?)\n    \)', content)
    if not m:
        print(section_name + ' NOT FOUND')
        return
    body = m.group(1)
    def repl_entry(match):
        c, v = match.group(1), match.group(2)
        if not v:
            v = src_dict.get(c, '')
        return '"' + c + '" to "' + v + '"'
    new_body = re.sub(r'"([^"]+)" to "([^"]*)"', repl_entry, body)
    content = content.replace('private val ' + section_name + ' = mapOf(' + body, 'private val ' + section_name + ' = mapOf(' + new_body)

